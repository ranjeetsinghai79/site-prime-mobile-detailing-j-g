function generatePageTsx(niche: string, heroLayoutId: string): string | null {
  const pg = NICHE_PAGE[niche]
  if (!pg) return null

  const isCentered = heroLayoutId === 'centered-editorial'
  const isBento    = heroLayoutId === 'bento-hero'
  if (!isCentered && !isBento) return null

  const heroImport = isCentered ? 'HeroCentered' : 'HeroBento'
  const heroJsx    = isCentered
    ? `<HeroCentered config={config} posterSrc="/hero-1.jpg" />`
    : `<HeroBento config={config} photoSrc="/hero-1.jpg" />`

  // ctaJsx uses template literal syntax — single-quoted in our object, backtick-wrapped in output
  const ctaLine = `{\`${pg.ctaJsx}\`}`

  return [
    `import { Nav, ${heroImport}, WhyUs, Reviews, ServiceAreas, Contact, Footer } from "@core/web"`,
    pg.localImports,
    `import { config } from "@/lib/config"`,
    ``,
    `export default function Home() {`,
    `  return (`,
    `    <>`,
    `      <Nav config={config} scrolledTheme="light" />`,
    `      <main>`,
    `        ${heroJsx}`,
    `        ${pg.nicheSection}`,
    `        <WhyUs config={config} />`,
    `        <Reviews`,
    `          config={config}`,
    `          ctaText=${ctaLine}`,
    `        />`,
    `        <ServiceAreas config={config} />`,
    `        <Contact`,
    `          config={config}`,
    `          heading="${pg.contactHeading}"`,
    `          paragraph="${pg.contactParagraph}"`,
    `          submitText="${pg.contactSubmit}"`,
    `        />`,
    `      </main>`,
    `      <Footer config={config} />`,
    `    </>`,
    `  )`,
    `}`,
    ``,
  ].join('\n')
}

function slugify(name: string) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 40)
}

