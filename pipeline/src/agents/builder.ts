// ─── Brand color CSS generator ────────────────────────────────────────────────
// Injects per-client brand colors as CSS variable overrides into globals.css.
// Scoped to [data-theme="<theme>"] so they take precedence over preset tokens.

const HEX6 = /^#[0-9A-Fa-f]{6}$/

function hexToRgb(hex: string): [number, number, number] {
  const n = parseInt(hex.slice(1), 16)
  return [(n >> 16) & 255, (n >> 8) & 255, n & 255]
}

function rgbToHex(r: number, g: number, b: number): string {
  return '#' + [r, g, b].map(v => Math.max(0, Math.min(255, Math.round(v))).toString(16).padStart(2, '0')).join('')
}

function darken(hex: string, factor: number): string {
  const [r, g, b] = hexToRgb(hex)
  return rgbToHex(r * factor, g * factor, b * factor)
}

function lighten(hex: string, factor: number): string {
  const [r, g, b] = hexToRgb(hex)
  return rgbToHex(r + (255 - r) * factor, g + (255 - g) * factor, b + (255 - b) * factor)
}

function hexToRgba(hex: string, alpha: number): string {
  const [r, g, b] = hexToRgb(hex)
  return `rgba(${r}, ${g}, ${b}, ${alpha})`
}

function generateBrandColorCSS(
  colors: { primary?: string; secondary?: string; accent?: string },
  theme: string
): string | null {
  const primary = colors.primary && HEX6.test(colors.primary) ? colors.primary : null
  const accent  = colors.accent  && HEX6.test(colors.accent)  ? colors.accent  : primary

  if (!primary && !accent) return null

  const base    = primary ?? accent!
  const hi      = accent  ?? primary!

  const lines = [
    `/* ── Client brand color override (generated by pipeline) ── */`,
    `[data-theme="${theme}"] {`,
    `  --brand-bg:           ${darken(base, 0.08)};`,
    `  --brand-bg-mid:       ${darken(base, 0.12)};`,
    `  --brand-bg-section:   ${darken(base, 0.10)};`,
    `  --brand-accent:       ${hi};`,
    `  --brand-accent-light: ${lighten(hi, 0.20)};`,
    `  --brand-accent-glow:  ${hexToRgba(hi, 0.40)};`,
    `  --brand-grad-from:    ${lighten(hi, 0.12)};`,
    `  --brand-grad-to:      ${hi};`,
    `  --brand-rule:         ${hi};`,
    `  --brand-blob-1:       ${hexToRgba(hi, 0.30)};`,
    `  --brand-blob-2:       ${hexToRgba(lighten(hi, 0.25), 0.20)};`,
    `  --brand-particle:     ${hi};`,
    `}`,
  ]

  return lines.join('\n')
}

